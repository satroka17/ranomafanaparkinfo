                     <h2>Arboretum</h2>
                      <p class="margin_0">The Ranomafana Arboretum was founded in 1991 by Dan Turk. It is located just 4 km east of Ranomafana on National Route 25 - a two minute drive or a short and easy 20 minute walk from town. It houses over 50 species of trees, some native to the southeast of Madagascar and some brought to the island through international trade centuries ago. It is also home to lowland-streaked tenrecs, chameleons, butterflies, frogs, and all sorts of interesting insects. </p>
                      <p class="margin_0">Once you arrive at the arboretum, you will pass a ticket booth. Tickets for non-Malagasy guests are 10 000 MGA per person. You will be given a guide who speaks English, French and Malagasy, and then begin a small loop of the grounds. Expect the tour to take upwards of 1 hour, and be prepared to feed a giant Parson’s chameleon an insect snack! </p>
                      <p class="margin_0">The arboretum is a hidden gem of Ranomafana. It offers stunning views of the valley, interesting fruits to try, and a fascinating history of trees in the region. It also houses one young baobab tree. Make sure to visit this special place.</p>

