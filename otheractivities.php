                      <p class="margin_0">For the more adventurous groups and individuals, you might consider taking a kayaking trip down the Namorana river! Trips can be arranged through Varibolo Kayaking, who have a physical shop near the entrance to the national park and can also be reached at +1 516 479 1889, +261 034 90 823 03, and info@varibolo.com. No matter if you contact them in person or online/phone, they can assist you with setting up a time for your excursion. Prices are 60 000 MGA per person, and the whole trip can be expected to take about two hours. They will confirm, but typically you will meet Varibolo staff at the little island at the middle of the river to the thermal pool/baths, where they will provide you with general instruction and of course the kayak itself. Along your way down the river (which loosely follows National Route 25), you might encounter people panning for gold, and have the ability to stop at a toaka gasy (local moonshine) “distillery”.</p>
